#naming conventions

# SNAKE CASE ,  PASCAL CASE , CAMEL CASE 

# SNAKE CASE
# --> All Small letters seperated by under score.
# EX: user_name,id_name ........
# humanbeing-->human_being
# USED IN PYTHON

# PASCAL CASE 
# -->each word starts with capital letter
# humanbeing-->HumanBeing
# EX: UserName , Password , IdName

# CAMEL CASE
# --> first word starts with small letter Second word starts with capital letter.
# EX: userName , password , idName
# humanbeing-->humanBeing
# NOT USED IN PYTHON